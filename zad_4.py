cena = 10.0
waga = 2.5
naleznosc = cena * waga # lepiej pisać ze spacjami (czytelniejsze)

print('Cena za kg:', cena)
print('Waga:', waga)
print('Należność:', naleznosc)

# f-string - wykorzystuje się do budowania napisów, które zawierają zmienne
print(f'Cena za kg: {cena} zł')
print(f'Waga: {cena} kg')
print(f'Należność: {naleznosc} zł')

